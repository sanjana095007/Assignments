package com.xworkz.Instance.Airport;

public class Airport {
    public Airport() {
        System.out.println("No-arg constructor of Airport");
    }

    public void land() {
        System.out.println("An airplane is landing at the airport");
    }

    public void takeOff() {
        System.out.println("An airplane is taking off from the airport");
    }

    public void checkIn() {
        System.out.println("Passengers are checking in at the airport");
    }

    public void securityCheck() {
        System.out.println("Security check is being conducted at the airport");
    }

    public void board() {
        System.out.println("Passengers are boarding the flight");
    }
}

