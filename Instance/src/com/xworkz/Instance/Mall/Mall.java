package com.xworkz.Instance.Mall;

public class Mall {

    public Mall() {
        System.out.println("No-arg constructor of Mall.");
    }

    public void open() {
        System.out.println("Mall is opening");
    }

    public void close() {
        System.out.println("Mall is closing");
    }

    public void shop() {
        System.out.println("People are shopping in the mall");
    }

    public void eat() {
        System.out.println("People are dining in the food court");
    }

    public void entertain() {
        System.out.println("Mall is offering entertainment options");
    }
}

